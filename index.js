// - **feat**: Добавление новой функциональности (`feat: добавлен поиск по товарам`)  
// - **fix**: Исправление ошибки (`fix: исправлен баг с авторизацией`)  
// - **chore**: Технические изменения, не влияющие на код (`chore: обновлены зависимости`)  
// - **refactor**: Улучшение кода без изменения логики (`refactor: оптимизирован алгоритм сортировки`)  
// - **perf**: Улучшение производительности (`perf: уменьшено время загрузки страницы`)  
// - **docs**: Обновление документации (`docs: добавлена инструкция по установке`)  
// - **test**: Добавление или исправление тестов (`test: добавлены unit-тесты для API`)  
// - **style**: Изменение стиля кода (отступы, запятые, кавычки) (`style: исправлены пробелы и форматирование`)  
// - **ci**: Изменения в CI/CD (`ci: обновлен workflow GitHub Actions`)  
// - **build**: Изменения в сборке проекта (`build: обновлена версия Webpack`)  
// - **revert**: Откат изменений (`revert: отменен коммит с удалением логирования`)  
// - **breaking**: Внесены критические изменения, ломающие обратную совместимость (`breaking: удален старый API v1`)  
// - **wip** (work in progress) – работа в процессе (`wip: верстаю новую страницу профиля`)  
// - **hotfix** – срочное исправление бага (`hotfix: исправлен критический баг в продакшене`)  
// - **merge** – слияние веток (`merge: слияние feature/profile в main`)  
// - **security** – исправление уязвимости (`security: обновлены зависимости из-за уязвимости в библиотеке`)  
